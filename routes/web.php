//Cache clearing route
Route::get('/clear-cache', function (Router $route) {
    $r = $route->getRoutes();
    foreach ($r as $value) {
        echo $value->uri();
        echo "<br/>";
    }

    Artisan::call('cache:clear');
    Artisan::call('config:clear');
    Artisan::call('route:clear');
    Artisan::call('view:clear');
    return 'Application cache cleared';
});

